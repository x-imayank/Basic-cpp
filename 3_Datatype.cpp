#include <bits/stdc++.h> //This contains all the library in c++.
using namespace std;

int main()
{
    // int contains limited range of datatype to store.
    int x = 10, y;

    // long contains wider range of datatype to store.
    cin >> y;
    cout << y << endl;

    // long long contains much wider range of datatype to store.
    long long z = 15000000000;
    cout << z;

    // Float,double
    float a = 5.82;
    cout << endl << a;

    return 0;
}