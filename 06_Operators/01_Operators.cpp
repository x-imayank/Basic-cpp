// CPP Program to demonstrate the Binary Operators
#include <iostream>
using namespace std;

int main()
{
    int a = 8, b = 3;

    // Arithmetic operator
    cout << "a + b = " << (a + b) << endl;
    cout << "a - b = " << (a - b) << endl;
    cout << "a * b = " << (a * b) << endl;
    cout << "a / b = " << (a / b) << endl;
    cout << "a % b = " << (a % b) << endl;
    cout << endl
         << endl;
         
    // Relational operator
    cout << "a == b is " << (a == b) << endl;
    cout << "a > b is " << (a > b) << endl;
    cout << "a >= b is " << (a >= b) << endl;
    cout << "a < b is " << (a < b) << endl;
    cout << "a <= b is " << (a <= b) << endl;
    cout << "a != b is " << (a != b) << endl;

    return 0;
}
